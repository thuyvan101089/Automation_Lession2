package lesson03;

public class ForLoopLearning {
    public static void main(String[] args) {
        for(int i =0; i <5; i++){
            System.out.println(i++);
        }

        int a =0;
        for(;a<5;a++){
            System.out.println(a);
        }
        System.out.printf("a = %d",a);

    }
}
